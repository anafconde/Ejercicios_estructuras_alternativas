#Algoritmo que pida dos números e indique si el primero es mayor que el segundo o no.

numero1=int(input("Dame el primer número: "))
numero2=int(input("Dame el segundo número: "))

if numero1>numero2:
    print(f"El primer número {numero1} es mayor al segundo número {numero2}")
else:
    print(f"El primer número {numero1} es menor al segundo número {numero2}")